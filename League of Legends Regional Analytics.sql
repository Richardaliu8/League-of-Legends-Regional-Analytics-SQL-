    CREATE TABLE ChampionStats_TW
   (
    ChampionStatsID INT PRIMARY KEY AUTO_INCREMENT,
    RegionID INT,
    ChampionID INT,
    GamesPlayed INT,
    KDA DECIMAL(10,5),
    WinRate DECIMAL(5,5),
    PickRate DECIMAL(5,5),
    BanRate DECIMAL(5,5),
    CS DECIMAL(10,2),
    Gold DECIMAL(10,2),
    FOREIGN KEY (RegionID) REFERENCES Regions(RegionID),
