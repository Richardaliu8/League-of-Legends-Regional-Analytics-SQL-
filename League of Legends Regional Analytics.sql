-- create the database
DROP DATABASE IF EXISTS LeagueOfLegends;
CREATE DATABASE LeagueOfLegends;

-- select the database
USE LeagueOfLegends;

-- create the tables
CREATE TABLE Regions
(
  RegionID INT PRIMARY KEY AUTO_INCREMENT,
  RegionName VARCHAR(255) NOT NULL
);

CREATE TABLE Champions
( 
ChampionID INT PRIMARY KEY AUTO_INCREMENT,
  Champion VARCHAR(255) NOT NULL
);

DROP TABLE IF EXISTS ChampionStats_EUW;

CREATE TABLE ChampionStats_EUW
(
    ChampionStatsID INT PRIMARY KEY AUTO_INCREMENT,
    RegionID INT,
    ChampionID INT,
    GamesPlayed INT,
    KDA DECIMAL(10,5),
    WinRate DECIMAL(5,5),
    PickRate DECIMAL(5,5),
    BanRate DECIMAL(5,5),
    CS DECIMAL(10,2),
    Gold DECIMAL(10,2),
    FOREIGN KEY (RegionID) REFERENCES Regions(RegionID),
    FOREIGN KEY (ChampionID) REFERENCES Champions(ChampionID)
);

    
